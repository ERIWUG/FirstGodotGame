        "Безжалостный", "Отважный", "Мудрый", "Кровавый", "Непоколебимый", "Пылкий", "Шепчущий",
        "Ледяной", "Пламенный", "Громовой", "Невидимый", "Смертоносный", "Бессмертный", "Одинокий",
        "Верный", "Гордый", "Тихий", "Бешеный", "Великий", "Неистовый", "Терпеливый", "Спокойный",
        "Точный", "Везучий", "Роковой", "Серебряный", "Золотой", "Алмазный", "Призрачный"
    };

    private static List<string> _rareTitles = new List<string>
    {
        "Погибель Драконов", "Повелитель Бурь", "Страж Вечности", "Тень Королей", "Охотник на Магов",
        "Разрушитель Орд", "Защитник Слабых", "Мастер Клинка", "Хранитель Тайн", "Архитектор Судеб"
    };

    public static string Generate()
    {
        if (_firstNames.Count == 0) return "Безымянный";
        
        string name = _firstNames[GD.RandRange(0, _firstNames.Count - 1)];
        
        // 50% шанс получить обычный титул
        if (GD.Randf() < 0.5f)
        {
            string title = _titles[GD.RandRange(0, _titles.Count - 1)];
            return $"{name} {title}";
        }
        // 10% шанс получить редкий титул (исключаем одновременные обычные)
        if (GD.Randf() < 0.1f)
        {
            string rareTitle = _rareTitles[GD.RandRange(0, _rareTitles.Count - 1)];
            return $"{name} {rareTitle}";
        }
        return name;
    }

    private static List<string> _squadPrefixes = new List<string>
